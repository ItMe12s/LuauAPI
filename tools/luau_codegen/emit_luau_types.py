from __future__ import annotations

from collections import defaultdict
from typing import Dict, List, Sequence

from broma_parser import Class, Method, Root
from filtering import group_supported, linkless_class_names, prune_skipped_class_refs
from model import codegen_object_map, lua_namespace, object_classes, short_name
from type_map import TypeInfo, classify_arg, classify_return, enum_lua_names

LUAU_KEYWORDS = frozenset(
    {
        "and",
        "break",
        "do",
        "else",
        "elseif",
        "end",
        "false",
        "for",
        "function",
        "if",
        "in",
        "local",
        "nil",
        "not",
        "or",
        "repeat",
        "return",
        "then",
        "true",
        "until",
        "while",
        "continue",
        "export",
        "type",
        "typeof",
        "declare",
    }
)


def _ret(ret: TypeInfo) -> str:
    if ret.kind == "void":
        return "()"
    return ret.lua_type


def _fn(args: Sequence[TypeInfo], ret: TypeInfo, self_name: str | None = None) -> str:
    params = []
    if self_name:
        params.append(f"self: {self_name}")
    for i, arg in enumerate(args, start=1):
        params.append(f"arg{i}: {arg.lua_type}")
    return f"({', '.join(params)}) -> {_ret(ret)}"


def _classify_args(method: Method, objects: Dict[str, Class]) -> List[TypeInfo]:
    args: List[TypeInfo] = []
    for arg in method.args:
        info = classify_arg(arg.type, objects)
        assert info is not None
        args.append(info)
    return args


def _method_return_type(cls: Class, m: Method, objects: Dict[str, Class]) -> str:
    ret = classify_return(m.ret, objects)
    assert ret is not None
    out_types: List[str] = []
    if ret.kind != "void":
        out_types.append(ret.lua_type)
    if ret.kind == "void":
        for arg in m.args:
            info = classify_arg(arg.type, objects)
            assert info is not None
            if info.is_out:
                out_types.append(info.lua_type)
    if not out_types:
        return "()"
    if len(out_types) == 1:
        return out_types[0]
    return "(" + ", ".join(out_types) + ")"


def _method_type(cls: Class, methods: List[Method], objects: Dict[str, Class]) -> str:
    parts = []
    for m in methods:
        args = _classify_input_args(m, objects)
        ret_type = _method_return_type(cls, m, objects)
        params = []
        if not m.is_static:
            params.append(f"self: {cls.name}")
        for i, arg in enumerate(args, start=1):
            params.append(f"arg{i}: {arg.lua_type}")
        parts.append(f"({', '.join(params)}) -> {ret_type}")
    return " & ".join(f"({p})" for p in parts)


def _classify_input_args(m: Method, objects: Dict[str, Class]) -> List[TypeInfo]:
    ret = classify_return(m.ret, objects)
    assert ret is not None
    args: List[TypeInfo] = []
    for arg in m.args:
        info = classify_arg(arg.type, objects)
        assert info is not None
        if ret.kind == "void" and info.is_out:
            continue
        args.append(info)
    return args


